import math
# Exercício 3: Faça um programa que leia o valor de um ângulo e mostre seu seno, cosseno e tangente.

angulo = int(input('Insira um ângulo: '))
print('----------------------------------------')
print(f'O seno desse ângulo é {math.sin(angulo)}\nO coesseno desse ângulo é {math.cos(angulo)}\nA tangente desse ângulo é {math.tan(angulo)}')
print('----------------------------------------')


